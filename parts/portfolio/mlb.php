<!-- about the project -->
<p>When I was tasked with building a new MLB.TV application, I was estatic. I love the sport and was thrilled to updated my older, and outdated application I previously built for the original Boxee Box. I was also able to work with the same team from MLB which was great.</p>
<p>MLB.TV utilized HLS (HTTP Live Streaming). developed by Apple in 2009. This gave them the ability to steam live video in stunning hd quality, far exceeding any technology that has come before it. Similar to my <a href="/portfolio/pandora">Pandora</a> application, I utilized <code>JavaScript</code> &amp; <code>QML</code> to acheave a beautiful final result. Unfortunately due to time restrictions and client limitations some more advaced features of MLB.TV such as multi-game view where not possible.</p>

<!-- list of features, important info -->
<h3><small>MLB.TV Application Features</small></h3>
<ul>
  <li>Daily schedules with live scores.</li>
  <li>Playback of both home &amp; away video streams.</li>
  <li>Audio selection, when available, for each stream. (radio, ballpark only...).</li>
  <li>Caleder view showing past &amp; future games</li>
  <li>Sandings view of current leauge stadings for all teams.</li>
  <li>Ability to select your favorite team(s).</li>
  <li>Set score visibility on, off, or only off for your favorite teams' scores.</li>
  <li>Video quality could be set to HD, SD or Adaptive based on your connection.</li>
</ul>


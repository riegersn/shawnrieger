<!-- about the company/service -->
<p>Revision3 was a San Francisco-based multi-channel Internet television network that creates, produces and distributes Web television shows on niche topics. Founded in 2005, it has been operating as a subsidiary of Discovery Digital Networks since 2012. The network has primarily technology-based shows hosted and produced by Patrick Norton, and gaming shows hosted and produced by Adam Sessler. The network also creates and hosts comedic, political, DIY, and movie-related content. The name refers to the revisioning of video programming, according to founders Jay Adelson, Kevin Rose and David Prager. </p>


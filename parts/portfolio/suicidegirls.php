<!-- about the company/service -->
<p>SuicideGirls is an online community based website that revolves around pin-up photography sets of models known as the Suicide Girls. The website was founded in 2001 by Selena Mooney ("Missy Suicide") and Sean Suhl ("Spooky"). Most of the site is only accessible to paying members. It offers members access to images provided by models and photographers worldwide, as well as personal profiles, blogging platforms and the option to join numerous groups based upon different interests. There is also an online merchandise store offering a range of clothing, books and DVDs.</p>

<!-- divider -->
<hr>

<!-- about the project -->
<p>The SuicideGirls application was fun to build as the UI was designed to match the look &amp; feel of the their brand (at that time). The app featured full SuicideGirls goodness with all the videos, models and photo sets your heart desired. Starting off with a free set of pics and videos, users could log in with their accounts directly inside the application and get access to the entire sites media content on a big screen TV. Browse through all the models and their photos, sort and search, see the newest content and more. </p>
<p>The app was officially demoed and launched at the Boxee Meetup NYC event at Webster Hall in Brooklyn NY to great applause, although some of the SuicideGirls where there on stage stealing the spotlight!</p>

